    int row = 0;
    KhachHangDAO dao = new KhachHangDAO();

    private void inits() {
        fillTable();
        row = -1;
        updateStatus();
    }

    private void clearForm() {
        KhachHang kh = new KhachHang();
        this.setForm(kh);
        this.row = -1;
        updateStatus();
    }

    private void updateForm() {
          String err = check("Sua");
        if (err.equals("")) {
        KhachHang kh = getForm();
        try {
            dao.update(kh);
            fillTable();
            clearForm();
            tabs.setSelectedIndex(1);
            MsgBox.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Cập nhập thất bại");
        }
        } else {
            MsgBox.alert(this, err);
        }
    }

    private void insertForm() {
        String err = check("Them");
        if (err.equals("")) {
            KhachHang kh = getForm();
            try {
                dao.insert(kh);
                fillTable();
                clearForm();
                MsgBox.alert(this, "Thêm thành công");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(this, "Thêm thất bại");
            }
        } else {
            MsgBox.alert(this, err);
        }
    }

    private void deleteForm() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyển xóa");
        } else {
            String makh = txtMaKH.getText();
            if (makh.equals(Auth.user.getMaNV())) {
                MsgBox.alert(this, "không thể xóa chính bạn");
            } else if (MsgBox.confirm(this, "Bạn có muốn xóa khách hàng " + txtTenKH.getText() + "?")) {
                try {
                    dao.delete(makh);
                    fillTable();
                    clearForm();
                    MsgBox.alert(this, "Xóa thành công");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại");
                    e.printStackTrace();
                }
            }
        }
    }

    KhachHang getForm() {
        KhachHang kh = new KhachHang();
        kh.setMaKH(txtMaKH.getText());
        kh.setTenKH(txtTenKH.getText());
        kh.setSDT(txtSDT.getText());
        kh.setDiaChi(txtDiaChi.getText());
        return kh;
    }

    void setForm(KhachHang kh) {
        txtMaKH.setText(kh.getMaKH());
        txtTenKH.setText(kh.getTenKH());
        txtSDT.setText(kh.getSDT());
        txtDiaChi.setText(kh.getDiaChi());
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblKhachHang.getRowCount() - 1);
        //Trạng thái form
        txtMaKH.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = dao.selectAll();
            if (list != null) {
                for (KhachHang kh : list) {
                    model.addRow(new Object[]{kh.getMaKH(), kh.getTenKH(), kh.getSDT(), kh.getDiaChi()});
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "lỗi truy vấn dữ liệu");
        }
    }

    private void prev() {
        if (row > 0) {
            row--;
            edit();
        }
    }

    private void first() {
        if (row != 0) {
            row = 0;
            edit();
        }
    }

    private void last() {
        row = tblKhachHang.getRowCount() - 1;
        edit();
    }

    private void next() {
        row++;
        edit();
    }

    private void edit() {
        try {
            String makh = (String) tblKhachHang.getValueAt(row, 0);
            KhachHang kh = dao.selectById(makh);
            setForm(kh);
            tabs.setSelectedIndex(0);
            updateStatus();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private String check(String btn) {
        String err = "";
        if (!checkTrung() && btn.equals("Them")) {
            err += "Mã khách hàng  trùng ";
        } else {
            if (CheckData.isNull(txtMaKH.getText()) && btn.equals("Them")) {
                err += "Mã khách hàng không được để trống\n";
            }
            if (CheckData.isNull(txtTenKH.getText())) {
                err += "Tên khách hàng không được để trống\n";
            }
            if (CheckData.isNull(txtSDT.getText())) {
                err += "SDT không được để trống\n";
            } else {
                if (!CheckData.sdt(txtSDT.getText())) {
                    err += "SDT sai định dạng\n";
                }
            }
        }
        return err;
    }

    private boolean checkTrung() {
        KhachHang h = dao.selectById(txtMaKH.getText());
        if (h == null) {
            return true;
        } else {
            return false;
        }
    }

